#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int	len;

	len = 0;
	if (n == -2147483648)
	{
		len += ft_putstr("-2147483648");
		return (len);
	}
	if (n < 0)
	{
		ft_putchar('-', &len);
		n *= -1;
	}
	if (n >= 10)
		len += ft_putnbr(n / 10);
	ft_putchar(n % 10 + '0', &len);
	return (len);
}
